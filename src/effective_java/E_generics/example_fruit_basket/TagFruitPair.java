package effective_java.E_generics.example_fruit_basket;

public class TagFruitPair<K extends FruitTagI, V extends FruitI<?>> {
    private K key;
    private V value;

    public TagFruitPair(K key, V value) {
        this.key = key;
        this.value = value;
    }

    public K getKey() {
        return key;
    }

    public void setKey(K key) {
        this.key = key;
    }

    public V getValue() {
        return value;
    }

    public void setValue(V value) {
        this.value = value;
    }

    @Override
    public String toString() {
        return "Pair{" +
                "key=" + key +
                ", value=" + value +
                '}';
    }
}
